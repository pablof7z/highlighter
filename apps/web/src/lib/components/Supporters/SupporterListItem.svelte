<script lang="ts">
	import { NDKEvent, NDKKind, NDKSubscriptionReceipt, NostrEvent } from '@nostr-dev-kit/ndk';
	import { Hexpubkey } from '@nostr-dev-kit/ndk';
    import UserProfile from "$components/User/UserProfile.svelte";
	import { onDestroy } from 'svelte';
	import { Avatar, EventContent, Name } from '@nostr-dev-kit/ndk-svelte-components';
	import { getDefaultRelaySet } from '$utils/ndk';
	import RelativeTime from '$components/PageElements/RelativeTime.svelte';
	import { Textarea } from '$components/ui/textarea';
	import currentUser from '$stores/currentUser';
	import { ndk } from '$stores/ndk';

    export let creatorPubkey: Hexpubkey;
    export let pubkey: Hexpubkey;
    export let position: number;
    export let tierNames: Record<string, string> = {};
    export let tier: string;
    export let receipt: NDKSubscriptionReceipt;

    const substart = $ndk.storeSubscribe(
        { kinds: [NDKKind.Subscribe], "authors": [pubkey], "#p": [creatorPubkey] },
    );

    const supporterNote = $ndk.storeSubscribe(
        { kinds: [ NDKKind.GroupReply ], "#h": [creatorPubkey], "authors": [pubkey], "#e": [receipt.id] },
    );

    onDestroy(() => {
        substart.unsubscribe();
        supporterNote.unsubscribe();
    });

    let showForm = false;
    let note = "";

    async function publish() {
        const e = new NDKEvent($ndk, {
            content: note,
            kind: NDKKind.GroupReply,
            tags: [
                [ "h", creatorPubkey ],
            ]
        } as NostrEvent);
        e.tag(receipt);
        await e.publish(getDefaultRelaySet());
        showForm = false;
    }
</script>

<UserProfile {pubkey} let:userProfile let:fetching let:authorUrl>
    <a href={authorUrl} class="rounded flex flex-row gap-8 justify-between discussion-item">
        <div class="flex flex-row gap-4 justify-stretch grow">
            <a href={authorUrl} class="flex-none">
                <Avatar {pubkey} {userProfile} {fetching} class="w-24 h-24 border-2 border-black" type="square" />
            </a>
            <div class="flex flex-col items-start w-full">
                <Name {pubkey} {userProfile} {fetching} class="font-medium text-foreground text-xl" />
                {#if tier}
                    <div class="text-xs text-neutral-500">{tierNames[tier]??tier}</div>
                {/if}

                {#if $substart.length > 0}
                    <div class="text-xs text-neutral-400">
                        Supporter since
                        <RelativeTime event={$substart[0]} />
                    </div>
                {/if}

                {#if $supporterNote.length > 0}
                    <EventContent event={$supporterNote[0]} ndk={$ndk} class="text-base text-neutral-300" />
                {:else if $currentUser?.pubkey === pubkey && !showForm}
                    <button class="self-start button bg-white/10 text-foreground/60 mt-4 px-4 py-3 truncate max-w-[20rem]" on:click|stopImmediatePropagation|preventDefault={() => showForm = !showForm}>
                        Click to add a note
                    </button>
                {:else if showForm}
                    <!-- svelte-ignore a11y-click-events-have-key-events -->
                    <!-- svelte-ignore a11y-no-static-element-interactions -->
                    <div class="flex flex-col gap-2 w-full" on:click|stopImmediatePropagation|preventDefault={() => {}}>
                        <Textarea bind:value={note} class="textarea w-full" />
                        <div class="flex flex-row gap-4 items-end">
                            <button class="button" on:click={publish}>
                                Publish
                            </button>
                            <button on:click={() => showForm = false}>
                                Cancel
                            </button>
                        </div>
                    </div>
                {/if}
            </div>
        </div>

        <div class="flex flex-col gap-0 items-center justify-center bg-gradient w-[7rem] h-[7rem] mask mask-hexagon flex-none">
            <div class="text-5xl font-black text-foreground opacity-80 flex flex-row items-end">
                <span class="text-2xl opacity-80 absolute -translate-x-5">#</span>{position+1}
            </div>
            <div class="text-xs text-foreground">
                Supporter
            </div>
        </div>
    </a>
</UserProfile>
